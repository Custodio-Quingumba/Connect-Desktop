    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tb = (DefaultTableModel) tabela.getModel();
        boolean tem = false;

        for (int i = 0; i < tabela.getRowCount(); i++) {
            if (tb.getValueAt(i, 0).toString().equalsIgnoreCase(cbFuncionarios.getSelectedItem().toString())) {
                tem = true;
                break;
            }
        }

        if (!tem) {
            String funcionario = cbFuncionarios.getSelectedItem().toString();
            LoginOBJ func = new LoginOBJ();

            for (LoginOBJ login : logins) {
                if (login.getNome().equalsIgnoreCase(cbFuncionarios.getSelectedItem().toString())) {
                    func = login;
                }
            }
            int idCat = new Dados().getIdCategoria(func.getCategoria());

            String salarString = new Dados().getValorSalario(idCat);
            Object dado[] = {funcionario, func.getCategoria(), salarString};
            tb.addRow(dado);
        } else {
            JOptionPane.showMessageDialog(null, "Funcionário já adicionado");
        }

    }//GEN-LAST:event_btnAdicionarActionPerformed

