    private void BtnImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnImprimirActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tb = (DefaultTableModel) tabela.getModel();

        if (tb.getRowCount() > 0) {
            String ano = "" + new Dados().AnoServidor();
            boolean posso = false;
            for (int i = 0; i < tb.getRowCount(); i++) {
                String nome = tb.getValueAt(i, 0).toString();
                String função = tb.getValueAt(i, 1).toString();
                int idSalario = new Dados().getIdCategoria(função);
                FolhaOBJ folha = new FolhaOBJ(0, nome, idSalario, cbMeses.getSelectedIndex() + 1, ano);
                posso = new Dados().ConfirmaFolha(folha) == 0;
            }

            if (posso) {
                for (int i = 0; i < tb.getRowCount(); i++) {
                    String nome = tb.getValueAt(i, 0).toString();
                    String função = tb.getValueAt(i, 1).toString();
                    int idSalario = new Dados().getIdCategoria(função);
                    FolhaOBJ folha = new FolhaOBJ(0, nome, idSalario, cbMeses.getSelectedIndex() + 1, ano);
                    new Dados().RegistrarFolhaDeSalario(new Dados().getNomeUsuário(getIdUsuario()), folha);
                }
                int op = JOptionPane.showConfirmDialog(null, "Folha Criada, Imprimir?");
                if (op == 0) {
                    JOptionPane.showMessageDialog(null, "Sim");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Folha já existe");
            }

        } else {
            JOptionPane.showMessageDialog(null, "Folha Vazia");
        }
    }//GEN-LAST:event_BtnImprimirActionPerformed

