    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String cat = JOptionPane.showInputDialog(null, "INSIRA A CATEGORIA", "REGISTRO DE CATEGORIA", 1);

        if (new Dados().RegistrarTipoDeDespesa(new Dados().getNomeUsuário(getIdUsuario()), cat)) {
            cbTipo.removeAll();
            cbTipo.removeAllItems();
            despesa = new Dados().TiposDeDespesas();
            for (TipoDeDespesaOBJ desp : despesa) {
                cbTipo.addItem(desp.getNome().toUpperCase());
            }
            JOptionPane.showMessageDialog(null, "FEITO!");
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void txtValorKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtValorKeyReleased
        // TODO add your handling code here:
        char letra = evt.getKeyChar();
        if (!Character.isDigit(letra)) {
            evt.consume();
        }
    }//GEN-LAST:event_txtValorKeyReleased

