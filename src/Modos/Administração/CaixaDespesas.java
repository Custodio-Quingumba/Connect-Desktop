    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed

        NotaDeDespesaOBJ nota = new NotaDeDespesaOBJ(
                0,
                taDescrição.getText(),
                txtBeneficiario.getText(),
                new Dados().getIdDespesa(cbTipo.getSelectedItem().toString().toUpperCase()),
                txtValor.getText(),
                0,
                0,
                0,
                null
        );

        if (new Dados().RegistarDespesa(new Dados().getNomeUsuário(getIdUsuario()), nota)) {
            JOptionPane.showMessageDialog(null, "Despesa Registrada!");
        }

    }//GEN-LAST:event_btnConfirmarActionPerformed

