    private void tbEntradaMensalMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbEntradaMensalMouseClicked
        // TODO add your handling code here:
        int linha = tbEntradaMensal.getSelectedRow();
        modelo = (DefaultTableModel) tbEntradaMensal.getModel();
        if (linha != -1) {
            String Mes = modelo.getValueAt(linha, 1).toString();
            String ano = modelo.getValueAt(linha, 0).toString();
            List<FechoDeContasOBJ> f = new Dados().EntradaPorMesEspecifico(Mes, Integer.parseInt(ano));
            modelo = (DefaultTableModel) tbEntradaDiaria.getModel();
            modelo.setRowCount(0);
            for (FechoDeContasOBJ fe : f) {
                String[] val = {fe.getMes(), fe.getEntrada() + ""};
                modelo.addRow(val);
            }
        }
    }//GEN-LAST:event_tbEntradaMensalMouseClicked

