    private void tbSaidaMensalMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbSaidaMensalMouseClicked
        // TODO add your handling code here:
        modelo2 = (DefaultTableModel) tbSaidaMensal.getModel();
        int linha = tbSaidaMensal.getSelectedRow();
        if (linha != -1) {
            int idMes = new Dados().idMes(modelo2.getValueAt(linha, 1).toString());
            int ano = Integer.parseInt(modelo2.getValueAt(linha, 0).toString());
            List<SaidasPoDiaOBJ> sdia = new Dados().SaidasPorDia(idMes, ano);
            modelo2 = (DefaultTableModel) tbSaidaDiaria.getModel();
            modelo2.setRowCount(0);
            for (SaidasPoDiaOBJ saida : sdia) {
                String[] dado = {saida.getValor() + "", saida.getDia() + "", saida.getHora(), saida.getBeneficiario()};
                modelo2.addRow(dado);
            }
        }
    }//GEN-LAST:event_tbSaidaMensalMouseClicked

