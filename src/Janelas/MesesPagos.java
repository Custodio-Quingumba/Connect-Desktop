    private void btnAtualizarDiaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarDiaActionPerformed
        // TODO add your handling code here:
        if (!new Dados().BuscarDiaDePagamento(idCliente).equalsIgnoreCase("-1") && !new Dados().BuscarDiaDePagamento(idCliente).equalsIgnoreCase("0")) {
            int dia = Integer.parseInt(JOptionPane.showInputDialog(null, "Dia de pagamento atualizado"));
            if (new Dados().AtualizarDiaDePagamento(new Dados().getNomeUsuário(getIdUsuario()), idCliente, dia)) {
                JOptionPane.showMessageDialog(null, "Dia atualizado!");
            }
        } else {
            int dia = Integer.parseInt(JOptionPane.showInputDialog(null, "Dia de pagamento atualizado"));
            if (new Dados().AtualizarDiaDePagamento(new Dados().getNomeUsuário(getIdUsuario()), idCliente, dia)) {
                JOptionPane.showMessageDialog(null, "Dia Registrado!");
            }
        }
    }//GEN-LAST:event_btnAtualizarDiaActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        List<MesesOBJ> m;
        m = new Dados().TodosMeses();
        int idMes = 0;
        DefaultTableModel tb = (DefaultTableModel) Tabela.getModel();

        if (Tabela.getSelectedRow() != -1) {
            String Mes = Tabela.getValueAt(Tabela.getSelectedRow(), 1).toString();

            int Ano = Integer.parseInt(Tabela.getValueAt(Tabela.getSelectedRow(), 0).toString());;

            for (MesesOBJ meses : m) {
                if (meses.getNome().equalsIgnoreCase(Mes)) {
                    idMes = meses.getId();
                }
            }

            PagamentoOBJ pgm = new PagamentoOBJ(0, idCliente, idMes, Ano);
            String snh = JOptionPane.showInputDialog(null, "Insira a senha Raiz");

            if (new Dados().confirmar(snh)) {
                if (new Dados().ApagarPagamento(new Dados().getNomeUsuário(getIdUsuario()), pgm)) {
                    List<PagamentoOBJ> pgms;
                    pgms = new Dados().PagamentosFeitos(getIdCliente());
                    tb.setRowCount(0);
                    for (PagamentoOBJ pg : pgms) {
                        String ms = m.get(pg.getIdMes() - 1).getNome();
                        String ano = Integer.toString(pg.getAno());
                        String[] obj = {ano, ms};
                        tb.addRow(obj);
                    }
                    JOptionPane.showMessageDialog(null, "Registro Apagado!");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Senha Raiz errada! Processo Cancelado.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um registro");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

