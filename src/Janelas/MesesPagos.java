    public void atualizar(Objetos.ClientesOBJ cliente) {

        txtId.setText("" + getIdCliente());
        txtDia.setText("" + new Dados().getCliente(getIdCliente()).getDiaPagamento());
        txtNome.setText(cliente.getNome());
        txtContacto.setText(cliente.getTelefone());
        List<PagamentoOBJ> pgms;
        pgms = new Dados().PagamentosFeitos(getIdCliente());
        List<MesesOBJ> m;
        m = new Dados().TodosMeses();

        DefaultTableModel tb = (DefaultTableModel) Tabela.getModel();
        tb.setRowCount(0);
        for (PagamentoOBJ pgm : pgms) {
            String ms = m.get(pgm.getIdMes() - 1).getNome();
            String ano = Integer.toString(pgm.getAno());
            String[] obj = {ano, ms};
            tb.addRow(obj);
        }
    }

