    private void TabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabelaMouseClicked
        // TODO add your handling code here:
        DefaultTableModel tb = (DefaultTableModel) Tabela.getModel();
        int linha = Tabela.getSelectedRow();
        String n = tb.getValueAt(linha, 1).toString();
        int id = Integer.parseInt(tb.getValueAt(linha, 0).toString());
        List<Objetos.ClientesOBJ> cliente = new Dados().Todos();
        
        if (linha != -1) {
            for (Objetos.ClientesOBJ clientes : cliente) {
                if (n.equalsIgnoreCase(clientes.getNome())) {
                    Nome.setText(clientes.getNome());
                    Nacionalidade.setText(clientes.getNacionalidade());
                    Endereço.setText(clientes.getEndereço());
                    Email.setText(clientes.getEmail());
                    Contacto.setText(clientes.getTelefone());
                    cbPacote.setSelectedIndex(clientes.getPacote() - 1);
                    Serviço.setText(clientes.getServiço());
                    BI.setText(clientes.getBI());
                    GPON.setText(clientes.getGPON());
                    if (clientes.getInformação().equalsIgnoreCase("ACTIVADO")) {
                        btnActDes.setText("DESATIVAR");
                    } else {
                        btnActDes.setText("ACTIVAR");
                    }
                    
                    taNota.setText(new Dados().BuscarNota(id));
                    
                    vlan.setText(clientes.getVlan());
                }
            }
        } else {
            Nome.setText("");
            Nacionalidade.setText("");
            Endereço.setText("");
            Email.setText("");
            Contacto.setText("");
            cbPacote.setSelectedIndex(0);
            Serviço.setText("");
            btnActDes.setText("...");
            GPON.setText("");
            BI.setText("");
            vlan.setText("");
        }
    }//GEN-LAST:event_TabelaMouseClicked

