    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tb = (DefaultTableModel) Tabela.getModel();
        if (Tabela.getSelectedRow() != -1) {
            int linha = Tabela.getSelectedRow();
            int id = Integer.parseInt(tb.getValueAt(linha, 0).toString());
            int cod = new Dados().BuscarIdHistorico(id);
            
            if (new Dados().atualizarHistorico(cod, "CANCELADO -" + new Dados().getNomeUsuário(getIdUsuario()))) {
                if (new Dados().CancelarAtivaçãoDesativação(new Dados().getNomeUsuário(getIdUsuario()), id) && new Dados().Desracionar(id)) {
                    JOptionPane.showMessageDialog(null, "Ativação ou Desativação Cancelada!");
                }
            }
        }
    }//GEN-LAST:event_btnCancelarActionPerformed

