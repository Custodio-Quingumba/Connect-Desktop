    private void btnActDesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActDesActionPerformed
        
        DefaultTableModel tb = (DefaultTableModel) Tabela.getModel();
        if (Tabela.getSelectedRow() != -1) {
            int linha = Tabela.getSelectedRow();
            int id = Integer.parseInt(tb.getValueAt(linha, 0).toString());
            
            int cod = new Dados().BuscarIdHistorico(id);
            
            if (btnActDes.getText().equalsIgnoreCase("Desativar")) {
                
                if (new Dados().atualizarHistorico(cod, new Dados().getNomeUsuário(getIdUsuario()))) {
                    if (new Dados().Desativar(new Dados().getNomeUsuário(getIdUsuario()), id) && new Dados().Desracionar(id)) {
                        JOptionPane.showMessageDialog(null, "Cliente Desativado!");
                    }
                }
            } else {
                if (new Dados().atualizarHistorico(cod, new Dados().getNomeUsuário(getIdUsuario()))) {
                    if (new Dados().Ativar(new Dados().getNomeUsuário(getIdUsuario()), id) && new Dados().Desracionar(id)) {
                        JOptionPane.showMessageDialog(null, "Cliente Activado!");
                    }
                }
            }
            tb.setRowCount(0);
            List<Objetos.ClientesOBJ> cliente = new Dados().AllRacionar();
            
            for (Objetos.ClientesOBJ clientes : cliente) {
                String[] dados = {Integer.toString(clientes.getId()), clientes.getNome(), clientes.getInformação()};
                tb.addRow(dados);
            }
            
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um cliente");
        }
    }//GEN-LAST:event_btnActDesActionPerformed

