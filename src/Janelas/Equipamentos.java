    private void btnRecarregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRecarregarActionPerformed
        // TODO add your handling code here:
        List<FornecedoresOBJ> fornecedores = new Dados().TodosFornecedor();
        List<EquipamentoOBJ> equipamento = new Dados().TodosEquipamentos();
        cbFornecedor.removeAllItems();
        for (FornecedoresOBJ fornecedore : fornecedores) {
            cbFornecedor.addItem(fornecedore.getNome().toUpperCase());
        }
        DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();
        modelo.setRowCount(0);
        for (EquipamentoOBJ equipamentoOBJ : equipamento) {
            String dados[] = {"" + equipamentoOBJ.getId(), equipamentoOBJ.getNome(), equipamentoOBJ.getFornecedor(), "" + equipamentoOBJ.getQtd()};
            modelo.addRow(dados);
        }

        btnRegistrar.setEnabled(true);
        txtNome.setText("");
        txtModelo.setText("");
        txtPreço.setText("");
        txtQtd.setText("");
        txtNome.setEditable(true);
        txtModelo.setEditable(true);
        txtPreço.setEditable(true);
        txtQtd.setEditable(true);
    }//GEN-LAST:event_btnRecarregarActionPerformed

