    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        try {
            String nome = txtNome.getText().toUpperCase();
            String modelo = txtModelo.getText().toUpperCase();
            int quantidade = Integer.parseInt(txtQtd.getText());
            float preço = Float.parseFloat(txtPreço.getText());
            String fornecedor = cbFornecedor.getSelectedItem().toString().toUpperCase();

            if (new Dados().RegistrarEquipamento(new Dados().getNomeUsuário(getIdUsuario()), new EquipamentoOBJ(0, nome, modelo, quantidade, preço, fornecedor))) {
                JOptionPane.showMessageDialog(null, "FEITO!");
                txtNome.setText("");
                txtModelo.setText("");
                txtPreço.setText("");
                txtQtd.setText("");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro na interface: " + e);
        }

    }//GEN-LAST:event_btnRegistrarActionPerformed

