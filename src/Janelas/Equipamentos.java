    private void txtQtdKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtQtdKeyTyped
        char letra = evt.getKeyChar();
        if (!Character.isDigit(letra)) {
            evt.consume();
        }
    }//GEN-LAST:event_txtQtdKeyTyped

    private void txtPreçoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPreçoKeyTyped
        // TODO add your handling code here:
        char letra = evt.getKeyChar();
        String val = "" + letra;
        if ((!Character.isDigit(letra) && letra != '.')) {
            evt.consume();
        } else if (letra == '.') {
            if (txtPreço.getText().contains(".")) {
                evt.consume();
            }
        }
    }//GEN-LAST:event_txtPreçoKeyTyped

